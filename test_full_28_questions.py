def generate_random_answers():
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ª—É—á–∞–π–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã –Ω–∞ –≤—Å–µ 28 –≤–æ–ø—Ä–æ—Å–æ–≤"""
    questionnaire = get_full_questionnaire()
    answers = []
    
    # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å–ª—É—á–∞–π–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã —Å —É–∫–ª–æ–Ω–æ–º –≤ —Ç–æ–∫—Å–∏—á–Ω–æ—Å—Ç—å –¥–ª—è –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–≥–æ –∞–Ω–∞–ª–∏–∑–∞
    for q_id, q_data in questionnaire.items():
        # –°–æ–∑–¥–∞–µ–º –≤–µ—Å–æ–≤—ã–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç—ã - –±–æ–ª—å—à–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –≤—ã–±—Ä–∞—Ç—å —Ç–æ–∫—Å–∏—á–Ω—ã–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã
        weights = [0.1, 0.2, 0.3, 0.25, 0.15]  # –ë–æ–ª—å—à–µ –≤–µ—Å–∞ –Ω–∞ 3-5 –≤–∞—Ä–∏–∞–Ω—Ç—ã (—Ç–æ–∫—Å–∏—á–Ω—ã–µ)
        
        selected_index = random.choices(range(len(q_data["options"])), weights=weights)[0]
        selected_answer = q_data["options"][selected_index]
        
        answers.append({
            "question": q_data["question"],
            "answer": selected_answer,
            "question_id": q_id,
            "toxicity_level": selected_index + 1  # 1-5 —É—Ä–æ–≤–µ–Ω—å —Ç–æ–∫—Å–∏—á–Ω–æ—Å—Ç–∏
        })
    
    return answers

